
public class swap {
    public static void main(String[] args) {
        int x=100;
        int y=200;
        System.out.println("values of x "+x+"value of y"+y+"before swapping");
        int temp=x;
        x=y;
        y=temp;
        System.out.println("values of x "+x+"value of y"+y+"after swapping");
    }
}
